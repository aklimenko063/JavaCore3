package org.javaacademy.module2.homework.homework3.ex3;

public class Human {
    private double height;
    private int age;

    public Human(double height, int age) {
        this.height = height;
        this.age = age;
    }

    public double getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }
}
