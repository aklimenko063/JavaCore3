package org.javaacademy.module2.lessons.lesson6.ex5;

public class Children {
    private int height;
    private int age;

    public Children(int height, int age) {
        this.height = height;
        this.age = age;
    }

    public int getHeight() {
        return height;
    }

    public int getAge() {
        return age;
    }
}
