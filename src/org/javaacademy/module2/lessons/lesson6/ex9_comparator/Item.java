package org.javaacademy.module2.lessons.lesson6.ex9_comparator;

public class Item {
    private int height;
    private int width;
    private int length;

    public Item(int height, int width, int length) {
        this.height = height;
        this.width = width;
        this.length = length;
    }

    public int getHeight() {
        return height;
    }

    public int getWidth() {
        return width;
    }

    public int getLength() {
        return length;
    }
}
